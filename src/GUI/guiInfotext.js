function infoTextInit() {
  _.forOwn(infoText, function (value, key) {
    // console.log(key);
    // console.log(value.text);
    // console.log(value.boxID);
    // console.log(value.btnID);

    // writing text into hmtl that will be initialized as  info box
    document.getElementById(value.titID).innerHTML = value.titel;
    document.getElementById(value.texID).innerHTML = value.text;

    // initializing info box on hover (tippy.jy)
    const loc_iBox = document.getElementById(value.boxID);
    loc_iBox.style.display = "block";
    tippy(value.btnID, {
      theme: "infoBtn",
      content: loc_iBox,
      arrow: false,
      animation: "shift-away",
      placement: "right",
    });
  });
}
